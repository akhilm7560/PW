

//Named Function


function funName()
{
    console.log("Hi");
    
}
funName()


//Anonymous Function //Function expression


let funvar=function ()
{
    console.log("Hi");
    
}
funvar()

//Arrow Function

let funArrow= ()=> //fat arrow
{
    console.log("Hi, This is arrow function");
    
}
funArrow()


//IIFE- Immediately invoked function

(function(){
    console.log("Hi, This is IIFE function");
    
})
()
